# using index
List = ["item1", "item2", "item3"]
print("\nList Items: ")
print(List[0]) 
print(List[2])
  
# Creating a Multi-Dimensional List
# (By Nesting a list inside a List)
List = [['Have', 'A'] , ['GOOD'],['DAY'] ]
print("\nMulti-Dimensional List: ")
print(List)
